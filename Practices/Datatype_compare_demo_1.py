#!/usr/bin/env python3

x = []
y = ()
z = {}
z_1 = set()

print('Datatypes : ')
print('x = [] \t',type(x))
print('y = () \t',type(y))
print('z = {} \t',type(z))
print('z_1 = set() \t',type(z_1))

print('IDs :')
print(id(x))
print(id(y))
print(id(z))
print(id(z_1))

print('Lengths : ')
print(len(x))
print(len(y))
print(len(z))
print(len(z_1))
